#include <stdio.h>

int main() {
    int m1, m2, m3;
    float result;
    printf("Введіть 3 значення через пробіл:\n");
    scanf("%d %d %d", &m1, &m2, &m3); //отримуємо числа від користувача
    result = 1/(1.0/m1 + 1.0/m2 + 1.0/m3); //розрахунок результату через потужність, роботу та час
    printf("Час, необхідний для з'їдання пирога: %.2f годин", result);
    return 0;
}
