#include <stdio.h>

int main() {
    int n, result = 0;
    printf("Введіть натуральне число:\n");
    scanf("%d", &n);
    for (int i = n; i > 0; i--){
        if (n%i == n/i){
            result++;
        }//умова, в якій порівнюють частку та залишок і додають 1 до результата, якщо істина
    } //цикл, в якому переглядаються всі числа, менші за задане
    printf("Кількість рівних дільників: %d", result);
    return 0;
}
